CREATE DEFINER=`root`@`localhost` PROCEDURE `sp_guardar_materia` (IN `p_usuario` INT, IN `p_grupo` INT)   BEGIN
    DECLARE grupo_existe INT;
    
    -- Verificar si el grupo existe
    SELECT COUNT(*) INTO grupo_existe FROM GRUPOS WHERE ClvGrupo = p_grupo;
    
    IF grupo_existe > 0 THEN
        INSERT INTO HORARIOS_GUARDADOS (ClvUsuario, ClvGrupo) VALUES (p_usuario, p_grupo);
    ELSE
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Error: El grupo no existe.';
    END IF;
END$$

